#include "libft.h"

char	*ft_strdup(char *str)
{
	char	*result;
	int		i;

	result = malloc (sizeof(char) * ft_strlen(str) + 1);
	if (!result)
		return (0);
	i = 0;
	while (str[i])
	{
		result[i] = str[i];
		i++;
	}
	result[i] = '\0';
	return (result);
}
